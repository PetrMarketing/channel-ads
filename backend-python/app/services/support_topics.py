def get_topic(topic_id: str) -> dict | None:
    """Возвращает узел дерева по id или None если не найден."""
    return TOPICS.get(topic_id)


def get_topics_tree() -> dict:
    """Возвращает плоский dict для фронта. Для каждого узла дополнительно
    раскрываются children как объекты {id, title}, чтобы фронту не делать
    второй запрос."""
    out = {}
    for tid, node in TOPICS.items():
        out[tid] = {
            "id": tid,
            "intro": node.get("intro"),
            "title": node.get("title"),
            "answer": node.get("answer"),
            "children": [
                {"id": cid, "title": TOPICS.get(cid, {}).get("title", cid)}
                for cid in node.get("children", [])
            ],
        }
    return out
