from ..config import settings


def get_support_prompt() -> str:
    bot = settings.MAX_BOT_USERNAME or "id575307462228_bot"
    app_url = settings.APP_URL or "https://itcakes.ru"
    return SUPPORT_PROMPT_TEMPLATE.format(bot=bot, app_url=app_url)


